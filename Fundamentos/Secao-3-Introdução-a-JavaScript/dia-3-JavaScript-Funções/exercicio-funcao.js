let saldo = 700;

function adicionaSaldo(valor) {
    return saldo + valor;
}
function subtraiSaldo(valor) {
    return saldo - valor;
}
function multiplicaSaldo(valor) {
    return saldo * valor;
}
function dividiSaldo(valor) {
    return saldo / valor;
}

console.log(adicionaSaldo(100));
console.log(subtraiSaldo(600));
console.log(multiplicaSaldo(3));
console.log(dividiSaldo(2));

